def validate_wgsl_with_naga(tmp_path, code):
    naga = shutil.which("naga")
    if naga is None:
        pytest.skip("naga is not installed")
    source = tmp_path / "shader.wgsl"
    source.write_text(code, encoding="utf-8")
    result = subprocess.run(
        [naga, str(source)],
        capture_output=True,
        text=True,
        timeout=60,
    )
    diagnostics = "\n".join(
        part for part in (result.stdout, result.stderr) if part.strip()
    )
    assert result.returncode == 0, diagnostics


def test_wgsl_backend_is_target_only():
    spec = codegen.get_backend("webgpu")

    assert spec is not None
    assert spec.name == "wgsl"
    assert spec.source_registry_name is None
    assert "wgsl" not in codegen.source_backend_names()
    assert codegen.normalize_backend_name("shader.wgsl") == "wgsl"
    assert codegen.get_backend_extension("webgpu") == ".wgsl"
    assert isinstance(codegen.get_codegen("wgsl"), WGSLCodeGen)


